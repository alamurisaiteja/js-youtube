const month = 3
switch (month) {
    case 1:
        console.log("january")
        break;
    case 2:
        console.log("feb")
        break;
    case 3:
        console.log("march")
        break;
    case 4:
        console.log("april")
        break;
    case 3:
        console.log("march matched again")
        break;

    default:
        console.log("default case matched")
        break;
}

